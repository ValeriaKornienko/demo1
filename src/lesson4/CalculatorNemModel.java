package lesson4;

public class CalculatorNemModel {
    public int sum(int x, int y){
        int result = x + y;
        return result;

    }
    public int sub(int x, int y){
        int result = x - y;
        return result;

    }
    public int mult(int x, int y){
        int result = x * y;
        return result;

    }
    public double div( int x, int y){
        return (double)  x / y;


    }
}
